										Atualmente a população de Feira Nova é de 21.444 habitantes,  segundo dados do IBGE.
										Com  a interiorização do crescimento, as cidades do interior pernambucano vêm tendo  um grande desenvolvimento habitacional e econômico	, fruto da chegada de várias  indústrias de nível nacional e internacional. Na região do empreendimento  apresentado estão instaladas fábricas como Nissim, WHB, Sadia/Perdigão, Red  Pack, Chemone entre outras que juntas estão gerando aproximadamente 10.000 (dez  mil) empregos diretos. O Alameda Feira Nova está localizado a 1,5 Km de duas  dessas fábricas.
										A Castro Lima Empreendimentos, observando o crescimento da região  e da Cidade de Feira Nova, investiu na compra de uma área de 16,5 Hectares,  localizada às margens da PE-50, distando 1,5 Km do novo Polo Industrial,  estando também junto ao centro do município. Lançando assim o Loteamento  Alameda Feira Nova.<br>
