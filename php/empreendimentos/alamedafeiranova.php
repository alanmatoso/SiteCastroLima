								O município de Feira Nova, também conhecido como a Terra da  Farinha, está localizado na mesorregião Agreste e na Microrregião Médio  Capibaribe do Estado de Pernambuco, limitando-se a norte com Limoeiro, a sul  com Glória do Goitá, a leste com Lagoa do Itaenga, e a oeste com Passira. A  área municipal ocupa 118,3 km² e representa 0,12% do Estado de Pernambuco. Fica  a 78,1 km da capital (Recife), cujo acesso é feito pela BR – 232, via PE – 050. <br>
